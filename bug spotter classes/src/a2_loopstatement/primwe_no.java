package a2_loopstatement;

public class primwe_no {
	

	public static void main(String[] args) {
		int no=7;
		int taj=0;
		
		 for(int a=6;a<=7-1;a++) {
			 if (no%a==0) {
				 taj=taj+1;
				 
				 
				 
			 }
			 if(taj==0) {
				 System.out.println(no+"is prime number");
			 }
			 else {
				 System.out.println(no+"is prime number");
			 }
				 
		 }
	}

}
